class DataType
{
	public static void main(String[] args) 
	{
		float a=10.5f;
		System.out.println("Inside a value is "+a);
		float b=20.5F;
		System.out.println("Inside a value is "+b);
		double c=30.5f;
		System.out.println("Inside a value is "+c);
		double d=40.5d;
		System.out.println("Inside a value is "+d);
		double e=50.5;
		System.out.println("Inside a value is "+e);
	}

}
